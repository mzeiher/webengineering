var number = 0n; // BigInt
var number = 1n; // floating point number
var number = 1n + 5n; // numbers can be added
var number = 5n - 5n; // numbers can be subtracted
var number = 10n / 5n; // numbers can be divided (only rounded to the next integer)
var number = 100n * 5n; // numbers can be multiplied
var number = 5n ** 2n; // numbers can be potentiated
var number = 10n % 5n; // also the modulo operator is supported
